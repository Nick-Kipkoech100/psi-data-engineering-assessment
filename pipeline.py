def normalize_date(col_name):
    """
    Handles two incoming formats:
      - YYYY-MM-DD  (ISO)
      - DD/MM/YYYY
    Returns a DateType column in ISO format.
    """
    return F.coalesce(
        F.to_date(F.col(col_name), "yyyy-MM-dd"),
        F.to_date(F.col(col_name), "dd/MM/yyyy")
    )

# Step 2 — Normalize date columns to ISO format (YYYY-MM-DD)

