# Using cleaned order table aas reference
orphaned_items = order_items_dedup.join(
    orders_clean.select("order_id"),
    on="order_id",
    how="left_anti"
)

print(f"Orphaned order_items (no matching order): {orphaned_items.count():,}")

# Writing the orphaned items to a separate output file
orphaned_items.write.mode("overwrite").csv("orphaned_items", header=True)
print("Orphaned items written to ./orphaned_items/")

#Left Join orderr to customers keeping clean order even if customer record is
#missing
orders_with_customers = orders_clean.join(
    customers_clean,
    on="customer_id",
    how="left"  # left join: orders are the primary table
)
print(f"\nAfter orders ← customers join: {orders_with_customers.count():,} rows")

# Inner join with order_items
# Inner join: only keep orders that have at least one line item
# Exclude orphaned items from this join

valid_items = order_items_dedup.join(
    orders_clean.select("order_id"),
    on="order_id",
    how="inner"
)

enriched = orders_with_customers.join(
    valid_items,
    on="order_id",
    how="inner"
)

print(f"After orders ← order_items join: {enriched.count():,} rows")


# Step 4 — Derive net_amount
# net_amount = total_amount * (1 - discount_pct / 100)

enriched = enriched.withColumn(
    "net_amount",
    F.round(
        F.col("total_amount") * (1 - F.col("discount_pct") / 100),
        2
    )
)

print("net_amount derived")
print("Task 03 complete — joins and enrichment done.")

