def load_csv(spark, path, schema, table_name):
    """
    Load CSV with explicit schema using PERMISSIVE mode.
    Returns df_clean and df_rejected separately.
    """
    df_raw = (
        spark.read
        .option("header", "true")
        .option("mode", "PERMISSIVE")
        .option("columnNameOfCorruptRecord", "_corrupt_record")
        .schema(schema)
        .csv(path)
    )

    if "_corrupt_record" not in df_raw.columns:
        df_raw = df_raw.withColumn("_corrupt_record", F.lit(None).cast(StringType()))

    df_rejected = (
        df_raw
        .filter(F.col("_corrupt_record").isNotNull())
        .withColumn("source_table", F.lit(table_name))
    )

    df_clean = df_raw.filter(F.col("_corrupt_record").isNull()).drop("_corrupt_record")

    total    = df_raw.count()
    rejected = df_rejected.count()
    clean    = df_clean.count()
    print(f"[{table_name}] total={total:,}  clean={clean:,}  rejected={rejected:,}")

    return df_clean, df_rejected

