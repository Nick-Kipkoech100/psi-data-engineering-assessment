from pyspark.sql.window import Window


# 1. Define Window for Customer Spending
customer_window = Window.partitionBy("customer_id").orderBy("order_date")

# 2. Add Running Total and Previous Transaction Amount
enriched_df = enriched.withColumn("running_total", F.sum("net_amount").over(customer_window)) \
                .withColumn("prev_amount", F.lag("net_amount", 1).over(customer_window)) \
                .withColumn("spending_change", F.col("net_amount") - F.col("prev_amount"))

# 3. Show Results without using .toPandas()
